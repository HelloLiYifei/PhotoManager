function interpolate(message, params) {
  if (!params) return message;
  return message.replace(/{{(\w+)}}/g, (_, key) => String(params[key] ?? ""));
}

const I18nContext = createContext(null);
const FALLBACK_I18N = {
  locale: "zh-CN",
  t: (key, params) => interpolate(resources["zh-CN"][key] ?? key, params),
  formatNumber: (value) => new Intl.NumberFormat("zh-CN").format(Number(value) || 0),
  formatBytes: (value) => {
    const bytes = Number(value) || 0;
    if (bytes < 1024) return `${bytes} B`;
    if (bytes < 1024 ** 2) return `${(bytes / 1024).toFixed(1)} KB`;
    if (bytes < 1024 ** 3) return `${(bytes / 1024 ** 2).toFixed(1)} MB`;
    return `${(bytes / 1024 ** 3).toFixed(1)} GB`;
  },
};

export function I18nProvider({ children }) {
  const { globalSettings } = useSettings();
  const locale = globalSettings.locale;

  const t = useCallback((key, params) => {
    const message = resources[locale]?.[key] ?? resources["zh-CN"][key] ?? key;
    return interpolate(message, params);
  }, [locale]);

  const formatNumber = useCallback(
    (value) => new Intl.NumberFormat(locale).format(Number(value) || 0),
    [locale],
  );

  const formatBytes = useCallback((value) => {
    const bytes = Number(value) || 0;
    if (bytes < 1024) return `${formatNumber(bytes)} B`;
    const units = ["KB", "MB", "GB", "TB"];
    let amount = bytes / 1024;
    let unitIndex = 0;
    while (amount >= 1024 && unitIndex < units.length - 1) {
      amount /= 1024;
      unitIndex += 1;
    }
    return `${new Intl.NumberFormat(locale, { maximumFractionDigits: amount < 10 ? 1 : 0 }).format(amount)} ${units[unitIndex]}`;
  }, [formatNumber, locale]);

  const value = useMemo(() => ({ locale, t, formatNumber, formatBytes }), [
    formatBytes,
    formatNumber,
    locale,
    t,
  ]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  return value ?? FALLBACK_I18N;
}
